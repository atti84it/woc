<h1>Project details</h1>

<p>WOC is a PHP application written on top of <a href="http://www.yiiframework.com">Yii framework</a>. Actually its core is a Yii module; 
this means that you can plug it into your Yii open source projects.</p>

<p>WOC is licensed under the terms of <a href="http://www.gnu.org/licenses/gpl.html">GPL license</a>.</p>

<p>You can find WOC sources <a href="https://github.com/atti84it/woc">here on github.com</a>.</p>

<p>The WOC name is inspired by James Surowiecki's book <a href="http://en.wikipedia.org/wiki/The_Wisdom_of_Crowds">&quot;The Wisdom of Crowds&quot;</a>.</p>

<p>The project home page is <a href="http://woc.isgreat.org">http://woc.isgreat.org</a>, 
and mantainer's home page is <a href="http://amberdev.altervista.org">http://amberdev.altervista.org</a>.</p>
